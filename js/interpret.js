// ── 별명 짓기 ────────────────────────────────────────────
IT.nickname = function (r) {
  const adj = IT.ILGAN[r.ilgan.char].adj;
  if (!r.dominant.length) return `${adj} 팔색조`;
  return `${adj} ${IT.SIPSIN[r.dominant[0]].role}`;
};

// ── 기질 리포트 ──────────────────────────────────────────
IT.buildReport = function (chart, r) {
  const B = [];
  const ig = IT.ILGAN[r.ilgan.char];
  const nick = IT.nickname(r);

  B.push({
    icon: ig.emoji, head: `당신은 "${nick}"입니다`,
    body: `<p>${ig.story}</p>` +
      `<p class="good">✨ <b>타고난 선물</b> — ${ig.gift}</p>` +
      `<p class="care">🍵 <b>살짝 챙길 것</b> — ${ig.care}</p>`,
  });

  // 강한 기질
  if (r.dominant.length) {
    let body = "";
    for (const name of r.dominant) {
      const s = IT.SIPSIN[name];
      body += `<p><b>${s.emoji} ${name} ${"●".repeat(Math.min(r.counts[name], 4))}</b> — ${s.scene} ${s.excess}</p>`;
    }
    body += `<p class="soft">이 기운들이 당신의 엔진입니다. 세다고 고치려 들 필요 없습니다. 어디에 쓸지만 정하면 됩니다.</p>`;
    B.push({ icon: "⚙️", head: "당신을 움직이는 엔진", body });
  } else {
    B.push({
      icon: "🎨", head: "당신을 움직이는 엔진",
      body: `<p>어느 한 기운도 튀지 않고 고르게 퍼져 있습니다. 이런 분들은 "나는 뭘 잘하는지 모르겠다"는 말을 자주 하십니다.</p>` +
        `<p>그런데 실은, 상황에 맞는 기질을 그때그때 꺼내 쓰는 사람입니다. 리더가 필요하면 리더가 되고, 들어줘야 할 땐 들어주는. 팔색조가 자기 색이 없는 게 아니라, 색이 여덟 개인 겁니다.</p>`,
    });
  }

  // 비어 있는 자리
  if (r.absent.length) {
    let body = `<p class="soft">여기서부터가 사실 제일 중요한 대목입니다. 없는 것은 흠이 아니라, 아직 안 쓴 근육입니다.</p>`;
    for (const name of r.absent.slice(0, 4)) {
      const s = IT.SIPSIN[name];
      body += `<p><b>${s.emoji} ${name}이 비어 있습니다</b> — ${s.absent}<br><span class="tip">→ ${s.tip}</span></p>`;
    }
    if (r.absent.length > 4)
      body += `<p class="soft">그 밖에 ${r.absent.slice(4).join(", ")}도 명식에 안 보입니다. 다만 사람은 여덟 글자로 다 담기지 않습니다. 살면서 만든 근육은 사주에 안 적혀 있어요.</p>`;
    if (r.pillarCount < 4)
      body += `<p class="soft">※ 태어난 시각을 모르셔서 여섯 글자로 본 결과입니다. 비어 있다는 판정은 가볍게만 받아들이세요.</p>`;
    B.push({ icon: "🕳️", head: "비어 있는 자리 (여기가 진짜 재밌습니다)", body });
  }

  // 오행
  {
    const ent = Object.entries(r.ohaeng);
    const maxN = Math.max(...ent.map(([, n]) => n));
    const many = ent.filter(([, n]) => n === maxN).map(([o]) => o);
    const zero = ent.filter(([, n]) => n === 0).map(([o]) => o);
    const MOOD = { "목": "자라고 뻗는", "화": "밝히고 드러내는", "토": "품고 중재하는", "금": "자르고 다듬는", "수": "흐르고 스며드는" };
    let body = `<p>당신 안에서 제일 크게 도는 기운은 <b>${many.map((o) => `${o}(${OHAENG_COLOR[o]})`).join("·")}</b> — ${many.map((o) => MOOD[o]).join(", ")} 기운입니다.</p>`;
    if (zero.length) {
      body += `<p><b>${zero.join("·")}</b>의 기운은 명식에 안 보입니다. 그런데 이건 결핍이라기보다 <b>당신이 평생 끌릴 방향</b>에 가깝습니다. 사람은 자기에게 없는 것에 이상하게 마음이 갑니다.</p>` +
        `<p class="tip">→ ${zero.map((o) => OHAENG_COLOR[o]).join("·")}색을 곁에 두거나, ${zero.map((o) => MOOD[o]).join("·")} 일을 취미로 두면 이상하게 숨이 트일 겁니다. 이름 풀이 탭에서 당신 이름이 이 기운을 채우고 있는지도 확인해 보세요.</p>`;
    } else {
      body += `<p>다섯 기운이 하나도 빠짐없이 다 들어 있습니다. 이런 명식이 생각보다 흔치 않습니다. 어느 쪽으로도 갈 수 있는 사람입니다. 🎉</p>`;
    }
    B.push({ icon: "🌈", head: "당신 안에서 도는 기운", body });
  }

  // 처방
  {
    const g = r.group, tips = [];
    const th = r.pillarCount >= 4 ? 4 : 3;
    if (g["관성"] + g["인성"] >= th)
      tips.push("<b>바깥으로 난 창이 많은 사람</b>입니다. 받아들이는 정보도, 신경 쓰는 시선도 많아서 가만히 있어도 피곤합니다. 하루 15분이라도 아무 입력 없는 시간(산책·명상·멍때리기)을 두세요. 이건 사치가 아니라 정비입니다.");
    if (g["식상"] >= 3)
      tips.push("<b>내보내야 사는 사람</b>입니다. 표현할 통로가 막히면 그 에너지가 안에서 상해요. 글이든 말이든 만들기든, 정기적으로 내보내는 자리를 하나 만드세요.");
    if (g["비겁"] >= 3)
      tips.push("<b>자기 중심이 단단한 사람</b>입니다. 대신 결정 전에 딱 한 사람에게만 물어보는 습관을 두세요. 그 한 명이 당신의 브레이크가 되어 줍니다.");
    if (g["재성"] >= 3)
      tips.push("<b>일을 잘 벌이고 잘 이루는 사람</b>입니다. 그래서 과로가 기본값입니다. 쉬는 날을 일정표에 '일'처럼 박아두는 것 — 이게 당신에게는 진짜 전략입니다.");
    if (r.absent.includes("편관"))
      tips.push("압박을 오래 견디는 게 특히 힘든 체질입니다. 참고 참다 무너지지 말고, 지치기 <b>전에</b> 쉬는 루틴을 미리 만들어 두세요.");
    if (r.absent.includes("정재"))
      tips.push("잔돈과 디테일 관리는 의지로 이기려 하지 마세요. 자동이체와 알림에 맡기면 그 에너지로 더 큰 걸 할 수 있습니다.");
    if (!tips.length)
      tips.push("특별히 무너질 구석이 안 보이는 균형형입니다. 대신 <b>지금 내가 어떤 기질을 쓰고 있는지</b>를 계절마다 한 번씩 돌아보세요. 당신의 강점은 선택할 수 있다는 것이니까요.");
    B.push({
      icon: "🧣", head: "그래서, 이렇게 살면 좋습니다",
      body: tips.map((t) => `<p>${t}</p>`).join("") +
        `<p class="soft">여기까지가 당신의 '기본값'입니다. 기본값은 운명이 아니라 출발점입니다. 사주가 정하는 건 당신이 어떤 사람이 될지가 아니라, 어떤 순간에 어떤 선택이 더 쉽게 나오는가 정도입니다. 그 선택이 쌓여서 성격이 되고, 성격이 삶이 됩니다. 그러니까 결국 — 당신이 만드는 겁니다.</p>`,
    });
  }
  return B;
};

