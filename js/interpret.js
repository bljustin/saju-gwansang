IT.namingHelper = function (sajuOhaeng) {
  if (typeof HANJA_DB === "undefined" || !sajuOhaeng) return null;
  const ent = Object.entries(sajuOhaeng);
  const zero = ent.filter(([, n]) => n === 0).map(([o]) => o);
  const minN = Math.min(...ent.map(([, n]) => n));
  const target = zero.length ? zero[0] : ent.filter(([, n]) => n === minN)[0][0];

  const cands = [];
  const seen = new Set();
  for (const reading of IT.NAME_SYLLABLES) {
    const rows = HANJA_DB[reading];
    if (!rows) continue;
    for (const [ch, won, pil, jw, hun] of rows) {
      if (jw !== target || seen.has(ch)) continue;
      if (pil < 4 || pil > 15) continue;          // 획수가 너무 적거나 많으면 제외
      const mean = IT.hunMeaning(hun);
      if (IT.HUN_BAD.test(mean)) continue;         // 이름에 피하는 뜻
      const score = IT.HUN_GOOD.test(mean) ? 2 : 1;
      seen.add(ch);
      cands.push({ ch, reading, won, pil, hun, score });
    }
  }
  cands.sort((a, b) => b.score - a.score || a.pil - b.pil);
  // 같은 음절이 몰리지 않게 음절당 최대 2자
  const perReading = {}, picks = [];
  for (const c of cands) {
    perReading[c.reading] = (perReading[c.reading] || 0) + 1;
    if (perReading[c.reading] > 2) continue;
    picks.push(c);
    if (picks.length >= 20) break;
  }
  return { target, picks };
};

