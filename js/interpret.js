/** 능선 SVG — 전통 81수리 분류를 나이대에 그대로 얹은 그림 (예언 아님) */
IT.suriRidgeSVG = function (bands) {
  const W = 640, H = 170, padL = 30, padR = 20, padT = 26, padB = 34;
  const xs = bands.map((_, i) => padL + ((W - padL - padR) * (i + 0.5)) / bands.length);
  const ys = bands.map((b) => padT + (H - padT - padB) * (1 - IT.SURI_CAT_LEVEL[b.s.cat] / 5.5));
  let d = `M ${padL - 10} ${ys[0] + 8} `;
  d += `L ${xs[0]} ${ys[0]} `;
  for (let i = 1; i < xs.length; i++) {
    const mx = (xs[i - 1] + xs[i]) / 2;
    d += `C ${mx} ${ys[i - 1]}, ${mx} ${ys[i]}, ${xs[i]} ${ys[i]} `;
  }
  d += `L ${W - padR + 10} ${ys[ys.length - 1] + 6}`;
  const CAT_COLOR = { "최상": "#196f4e", "상": "#2E7D5B", "길": "#5b8a5e", "중": "#B8860B", "흉": "#8a7a9e", "최흉": "#7a6a8e" };
  let dots = "", labels = "";
  bands.forEach((b, i) => {
    dots += `<circle cx="${xs[i]}" cy="${ys[i]}" r="5" fill="${CAT_COLOR[b.s.cat]}"/>`;
    labels += `<text x="${xs[i]}" y="${ys[i] - 11}" text-anchor="middle" font-size="12" font-weight="700" fill="${CAT_COLOR[b.s.cat]}">${b.s.reduced}수 ${b.s.name}</text>`;
    labels += `<text x="${xs[i]}" y="${H - 10}" text-anchor="middle" font-size="11" fill="#6b6154">${b.age.split(" ")[0]}</text>`;
  });
  return `<svg viewBox="0 0 ${W} ${H}" style="width:100%;height:auto" role="img" aria-label="이름 수리 능선">` +
    `<path d="${d}" fill="none" stroke="#8a3324" stroke-width="2.5" stroke-linecap="round" opacity=".75"/>` +
    dots + labels + `</svg>`;
};

