/** 지금 활성화된 기운들 (대운 2글자 + 세운 2글자) */
IT.activeNow = function (du, sn) {
  const list = [];
  if (du) list.push({ s: du.ganSipsin, src: "대운" }, { s: du.jiSipsin, src: "대운" });
  if (sn) list.push({ s: sn.ganSipsin, src: "올해" }, { s: sn.jiSipsin, src: "올해" });
  const groups = {};
  for (const x of list) {
    const g = SIPSIN_GROUP[x.s];
    groups[g] = (groups[g] || 0) + 1;
  }
  return { list, groups, has: (g) => (groups[g] || 0) > 0, strong: (g) => (groups[g] || 0) >= 2 };
};

/** 인생 지도에서 지금 위치 */
IT.lifeMap = function (du, nowY, birthY) {
  const age = nowY - birthY;
  const cur = du.periods.find((p) => nowY >= p.startYear && nowY < p.startYear + 10);
  const idx = du.periods.indexOf(cur);
  const next = du.periods[idx + 1];
  const left = cur ? (cur.startYear + 10) - nowY : null;
  const passed = idx >= 0 ? idx : 0;
  const stage = age < 30 ? "아직 길을 고르는 구간" : age < 45 ? "속도가 붙는 구간"
    : age < 60 ? "방향이 뚜렷해지는 구간" : "쌓은 것을 쓰는 구간";
  let body = `<p class="big">지금 당신은 <b>${age}세</b>, 인생의 <b>${stage}</b>에 있습니다.</p>`;
  if (cur) {
    const fl = IT.FLOW[SIPSIN_GROUP[cur.jiSipsin]];
    body += `<p>대운으로 보면 <b>${cur.startAge}~${cur.endAge}세 ${cur.pillar} 구간</b> — ${fl.emoji} <b>${fl.t}</b>의 10년을 지나는 중입니다. ` +
      `${passed > 0 ? `지금까지 ${passed}개의 대운을 건너오셨고, ` : ""}이 구간은 <b>앞으로 ${left}년</b> 남았습니다.</p>`;
    if (next) {
      const nf = IT.FLOW[SIPSIN_GROUP[next.jiSipsin]];
      body += `<p><b>다음 전환점은 ${cur.startYear + 10}년(${next.startAge}세)</b>, ${next.pillar} 대운으로 넘어갑니다. 그때부터는 ${nf.emoji} <b>${nf.t}</b>의 흐름이 시작됩니다. ` +
        (left <= 2
          ? `<b>전환이 코앞입니다.</b> 대운이 바뀌는 앞뒤 1~2년은 마음이 어수선하고 없던 생각이 드는 시기라, 큰 결정을 서두르기보다 관찰하는 편이 낫습니다.`
          : `아직 여유가 있으니, 지금 구간의 숙제를 마저 끝내두면 전환이 훨씬 매끄럽습니다.`) + `</p>`;
    }
  }
  body += `<p class="soft">대운이 바뀌는 해를 인생의 변곡점이라 부릅니다. 돌아보면 이사·이직·관계의 변화가 그 언저리에 몰려 있는 경우가 많습니다. 위 타임라인에서 지나온 구간을 눌러 확인해 보세요.</p>`;
  return body;
};

